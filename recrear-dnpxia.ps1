[System.IO.File]::WriteAllBytes($tempZip, [System.Convert]::FromBase64String($normalizedBase64))

try {
  $archive = [System.IO.Compression.ZipFile]::OpenRead($tempZip)

  foreach ($entry in $archive.Entries) {
    $targetPath = Join-Path $root $entry.FullName

    if ([string]::IsNullOrWhiteSpace($entry.Name)) {
      Ensure-Directory -Path $targetPath
      continue
    }

    $parent = Split-Path -Parent $targetPath
    Ensure-Directory -Path $parent

    [System.IO.Compression.ZipFileExtensions]::ExtractToFile($entry, $targetPath, $true) | Out-Null
    $relativeFile = $entry.FullName.Replace('/', '\')
    $createdFiles.Add($relativeFile)
  }

  $archive.Dispose()
}
finally {
  if (Test-Path -LiteralPath $tempZip) {
    Remove-Item -LiteralPath $tempZip -Force
  }
}

